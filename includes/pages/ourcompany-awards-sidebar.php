                        <h1>63rd Primetime Emmy Awards</h1><br />
                        <br /><h3>September 2011</h3><br />
						<p>Time Warner's divisions received a total of 29 Emmy wins, which includes awards received at the Creative Arts Emmy Awards on September 10.</p>
                        
                        
                        <br/>
                        <h1>38th Annual Emmy Awards</h1><br />
                        <br /><h3>June 2011</h3><br />
						<p>Warner Bros. received a total of five wins. </p>
						
                        
                        <br/>
                        <h1>36th Annual Gracie National</h1><br />
                        <br /><h3>May 2011</h3><br />
						<p>Time Warner divisions received a total of nine wins, including four for Turner Broadcasting, three for Warner Bros. and two for HBO. </p>
                        
                       <br/>
                        <h1>NAMIC Vision Awards </h1><br />
                        <br /><h3>May 2011</h3><br />
						<p>Time Warner divisions received a total of six wins, including three for HBO, two for Turner Broadcasting and one award for Warner Bros. </p>
                        
                        <br/>
                        <h1>15th Annual Webby Awards </h1><br />
                        <br /><h3>May 2011</h3><br />
						<p>Time Warner divisions received a total of six wins, including three for Turner Broadcasting, two for HBO and one for Time Inc. </p>
                        
                                                                  
                      						
                        
                       
                        
                                               
                        
                         