<h1>How do I perform a keyword search?</h1>
<p>The <b>Keyword</b> search field is a powerful tool that can help you find the exact jobs you are looking for.</p>
<p>You can search jobs for a single word or a phrase.   
Keyword searches effectively search as though a 
wildcard were appended to each search term. Thus a 
keyword search for <b>engineer</b> also finds 
engineering, engineered, engineers. Keyword searches will find jobs 
that contain matching words or phrases in any searchable job field including job title, job description, etc.
</p>
<p>Avoid using quotes or punctuation such as commas.</p>

<p>You can use boolean operators in the Keyword field to get more specific search results. 
These searches do an "exact match" search. That 
is, a boolean search for <b>asp OR engineer</b> 
finds only those exact forms of the two words. 
Use the * wildcard with search terms in boolean 
searches if you want other forms of the words 
considered in your results. Searching for <b>asp 
OR engineer*</b> will include engineering, 
engineered, engineers, etc, just as searching 
for <b>engineer</b> would.<p>Please note that non-English users must still use the English boolean operators. 
For example, if you are performing a boolean search in French, you cannot type "Unix ET NT", you must type "Unix AND NT".
</p>

